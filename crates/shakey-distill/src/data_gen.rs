use std::path::PathBuf;
use std::sync::Arc;
use tokio::sync::Mutex;

/// A single training example generated by a teacher.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct TrainingExample {
    /// Unique ID for deduplication
    pub id: String,
    /// The input prompt/instruction
    pub prompt: String,
    /// The teacher's completion
    pub completion: String,
    /// Which teacher model generated this
    pub teacher_model: String,
    /// The domain/topic category
    pub domain: String,
    /// Quality score from reward model (0.0 - 1.0, if scored)
    pub quality_score: Option<f64>,
    /// Timestamp of generation
    pub created_at: String,
    /// ZENITH: Native reasoning tokens for R1-style distillation
    pub reasoning_content: Option<String>,
    /// Per-token log probabilities (for logit distillation)
    pub token_logprobs: Option<Vec<f64>>,
}

impl shakey_core::training::trainer::AsTrainingExample for TrainingExample {
    fn id(&self) -> &str {
        &self.id
    }
    fn prompt(&self) -> &str {
        &self.prompt
    }
    fn completion(&self) -> &str {
        &self.completion
    }
    fn reasoning_content(&self) -> Option<&str> {
        self.reasoning_content.as_deref()
    }
}

