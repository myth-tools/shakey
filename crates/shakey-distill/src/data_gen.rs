    fn test_domain_teacher_role() {
        assert_eq!(Domain::CodeGeneration.teacher_role(), TeacherRole::Code);
        assert_eq!(Domain::Mathematics.teacher_role(), TeacherRole::Math);
        assert_eq!(
            Domain::GeneralKnowledge.teacher_role(),
            TeacherRole::Reasoning
        );
    }
