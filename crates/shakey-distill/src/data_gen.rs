//! Synthetic data generation pipeline for distillation.
//!
//! Uses teacher models to generate diverse, high-quality training data
//! for the student model. The pipeline:
//!
//! 1. **Topic Selection**: Choose what to learn next (from curriculum)
//! 2. **Prompt Generation**: Create diverse prompts for the topic
//! 3. **Teacher Querying**: Get responses from best teacher for each domain
//! 4. **Quality Scoring**: Use reward model to filter low-quality data
//! 5. **Formatting**: Convert to (input, target, teacher_logits) training tuples
//! 6. **Deduplication**: Avoid training on repeated content
//! 7. **Storage**: Save to disk in streaming-friendly format

