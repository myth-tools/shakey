    fn test_training_example_serialization() {
        let example = TrainingExample {
            id: "test-id".into(),
            prompt: "Hello".into(),
            completion: "World".into(),
            teacher_model: "test-model".into(),
            domain: "test-domain".into(),
            quality_score: Some(0.95),
            created_at: chrono::Utc::now().to_rfc3339(),
            reasoning_content: None,
            token_logprobs: None,
        };

        let json = serde_json::to_string(&example).unwrap();
        let deserialized: TrainingExample = serde_json::from_str(&json).unwrap();
        assert_eq!(deserialized.prompt, "Hello");
        assert_eq!(deserialized.quality_score, Some(0.95));
    }

